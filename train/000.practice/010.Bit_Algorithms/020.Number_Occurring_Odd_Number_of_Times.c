{
    int ar[] = {2, 3, 5, 4, 5, 2, 4, 3, 5, 2, 4, 4, 2};
    int n = sizeof(ar) / sizeof(ar[0]);

    // Function calling
    printf("Odd Occurrence number is : %d\r\n", getOddOccurrence(ar, n));
    return 0;
}